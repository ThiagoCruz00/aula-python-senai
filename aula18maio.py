# @title Exemplo de função - épar

def ehpar(wii):  # Assinatura da função
    return wii % 2 == 0  # Retorno da função: True se for par e False se for ímpar
ehpar(45)

# @title Exemplo de fução dentro de função - par ou ímpar

def par_ou_impar(p):
    if ehpar(p):
        return 'É par!'
    else:
        return 'É impar!'

par_ou_impar(123)

