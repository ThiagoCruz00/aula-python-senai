# @title Exemplo de função dentro de função - Média

lista = [4,87,123,12,4,28]
def media(lista):
    return sum(lista)/len(lista) # Sum é a função de soma
print(media(lista))

def somar(lst):   # Assinatura da função
    total = 0    # Declarando uma variável que armazena o total da lista
    for e in lst:  # Percorrer os elementos da lista
        total += e  # Adiciona e a variável total
    return total  # No fim da lista, obtemos a soma

def media(lista):
    resultado = somar(lista)/len(lista)  # Resultado terá a média da lista
    return resultado
lista = [4,87,13,1098,4,28]
media(lista)

